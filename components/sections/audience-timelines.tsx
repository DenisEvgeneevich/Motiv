function TimelineCard({ audience }: { audience: typeof audienceTimelines[0] }) {
  const [expandedPeriod, setExpandedPeriod] = useState<number | null>(0);

  return (
    <div 
      id={`timeline-${audience.id}`}
      className="glass-card-strong rounded-3xl p-6 scroll-mt-24"
    >
      {/* Header */}
      <div className="flex items-start gap-5 mb-6">
        <div className="w-16 h-16 rounded-2xl bg-white/20 flex items-center justify-center shrink-0 glow">
          <audience.icon className="w-8 h-8 text-white" />
        </div>
        <div className="flex-1 min-w-0 pt-0.5">
          <h3 className="text-xl font-bold text-white mb-2">{audience.title}</h3>
          <p className="text-sm text-white/70 leading-relaxed">{audience.goal}</p>
        </div>
      </div>

      {/* Focus */}
      <div className="glass-card rounded-2xl p-4 mb-6">
        <p className="text-sm text-white/80">
          <span className="font-semibold text-white">Акцент: </span>
          {audience.focus}
        </p>
      </div>

      {/* Timeline */}
      <div className="space-y-3">
        {audience.timeline.map((period, index) => (
          <div key={index} className="relative">
            <button
              onClick={() => setExpandedPeriod(expandedPeriod === index ? null : index)}
              className="w-full text-left glass-card rounded-2xl p-4 active:scale-[0.99] transition-all"
            >
              <div className="flex items-center justify-between gap-4">
                <div className="flex items-center gap-4 min-w-0">
                  <div className="w-10 h-10 rounded-xl bg-white/20 flex items-center justify-center shrink-0">
                    <span className="text-sm font-bold text-white">{index + 1}</span>
                  </div>
                  <div className="min-w-0">
                    <p className="text-sm font-semibold text-white/60 mb-0.5">{period.period}</p>
                    <p className="text-base font-bold text-white leading-snug">{period.title}</p>
                  </div>
                </div>
                <div className="shrink-0">
                  {expandedPeriod === index ? (
                    <ChevronUp className="w-5 h-5 text-white/50" />
                  ) : (
                    <ChevronDown className="w-5 h-5 text-white/50" />
                  )}
                </div>
              </div>
            </button>

            {/* Expanded content */}
            {expandedPeriod === index && (
              <div className="mt-2 px-4 pb-2">
                <div className="pl-14 space-y-2.5">
                  {period.actions.map((action, actionIndex) => (
                    <div key={actionIndex} className="flex items-start gap-3">
                      <div className="w-1.5 h-1.5 rounded-full bg-white/50 mt-2 shrink-0" />
                      <p className="text-sm text-white/75 leading-relaxed">{action}</p>
                    </div>
                  ))}
                </div>
              </div>
            )}
          </div>
        ))}
      </div>
    </div>
  );
}

